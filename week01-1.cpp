#include <stdio.h>
///studio的意思是標準的standard input/output
///有了stdio.h的外掛，才能用scanf() printf()
int main()
{
	int a, b; ///整數a,b
	scanf( "%d", &a ); ///克剛剛交掃描讀東西
	scanf( "%d", &b); ///可以用複製的再改b

	printf("%d", a+b ); ///實習課有教，印東西

}
